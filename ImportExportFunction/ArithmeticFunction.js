const Addition = (n1, n2) => {
    return n1 + n2;
}

const Substraction = (n1, n2) => {
    return n1 - n2;
}

const Division = (n1, n2) => {
    return n1 / n2;
}

const Multiplication = (n1, n2) => {
    return n1 * n2;
}


module.exports = {
    Addition: Addition,
    Substraction: Substraction,
    Division: Division,
    Multiplication: Multiplication,
};